# include<iostream> 
using namespace std;
int main(){ 
					// Operadores de Comparacion: Nos van a devolver un valor booleano (true(1) o  false(0))

// Operador Mayor que (>)
bool resultado=5>3;
cout<<resultado<<endl;

// Operador Menor que (<)
bool resultado2=5<3;
cout<<resultado2<<endl;

// Operador Menor que o igual a (<=)
bool resultado3=5<=3;
cout<<resultado3<<endl;

// Operador Mayor que o igual a (>=)
bool resultado4=5>=5;
cout<<resultado4<<endl;

// Operador Igual a (==)
bool resultado5=5==3;
cout<<resultado5<<endl;

// Operador diferente a (!=)
bool resultado6=5!=3;
cout<<resultado6;	
	return 0; 
}
