# include<iostream>
using namespace std;
int main(){
	// Operador de Incremento(++)
	int numero=0;
	numero++; // realiza el incremento en una unidad a la variable designada
	cout<<numero<<endl;
	
	//Operador de Decremento(--)
	int n2=100;
	n2--; // realiza el decremento en una unidad a la variable designada
	cout<<n2;
	

	return 0;
}
