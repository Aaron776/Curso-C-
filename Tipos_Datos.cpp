# include<iostream> 
using namespace std;
int main(){ 
	// Una variable es un espacio en memoria para guardar un dato, la variable puede tener el nombre que uno quiera
	
	// Tipos de Datos
	
	//Dato Entero (int): Son numero enteros
	int numero=15;
	cout<<numero <<endl;
	
	
	// Dato flotante (flot): son los numeros decimales
	 float numero2= 34.7;
	 cout<<numero2 <<endl;
	 
	 
	 // Dato double: pemite guarda nuemros decdimales con decimales mas grandes
	 double numero3=45.222;
	 cout<<numero3 <<endl;
	 
	 // Dato char: son datos de tipo letra y se udan comillas simples('')
	 char letra='a';
	 cout<<letra <<endl;
	 
	 // Dato string: son cadenas de caracteres como palabras, se usan las comillas dobles(" ")
	 string palabra ="Hola Mundo";
	 cout<<palabra <<endl;
	 

	
	return 0; 
}
